export function hello() {
    env.jsvm_js_contract_name(0)
    let contract_name = env.read_register(0)
    env.jsvm_method_name(1)
    let method_name = env.read_register(1)
    env.jsvm_args(2)
    let args = env.read_register(2)
    env.jsvm_account_id(3)
    let jsvm_account_id = env.read_register(3)
    env.predecessor_account_id(4)
    let caller_account = env.read_register(4)
    env.log(`js contract name (deployer's account id): ${contract_name}`)
    env.log(`call method name: ${method_name}`)
    env.log(`arguments: ${args}`)
    env.log(`jsvm (enclave) account id: ${jsvm_account_id}`)
    env.log(`caller account id: ${caller_account}`)
}